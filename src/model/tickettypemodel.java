package model;

public class tickettypemodel {

	private int tickettypenumber;
	private String tickettypename;
	private String tickettypedetail;

	public int getTickettypenumber() {
		return tickettypenumber;
	}

	public void setTickettypenumber(int tickettypenumber) {
		this.tickettypenumber = tickettypenumber;
	}

	public String getTickettypename() {
		return tickettypename;
	}

	public void setTickettypename(String tickettypename) {
		this.tickettypename = tickettypename;
	}

	public String getTickettypedetail() {
		return tickettypedetail;
	}

	public void setTickettypedetail(String tickettypedetail) {
		this.tickettypedetail = tickettypedetail;
	}

}
